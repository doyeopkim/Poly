package P_61;

public class ex_82 {

	public static void main(String[] args) {
		
		int i;
		double f;
		
		f = 5 / 4;
		System.out.println(f);
		
		f = (double) 5 / 4;
		System.out.println(f);
		
		i = (int) 1.3 + (int) 1.8;
		System.out.println(i);
		
		f = (int) 5 / (double) 4;
		System.out.println(f);
		
	}
}